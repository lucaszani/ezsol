        public void Listar(){
        try {
            OrdemServicoDAO dao = new OrdemServicoDAO();
            List<OrdemServico> listaOrdem = dao.ListaOrdemServico();
            
            DefaultTableModel modelo = (DefaultTableModel) tabelaOrdem.getModel();
            modelo.setNumRows(0);
            
            for(OrdemServico v : listaOrdem){
               modelo.addRow(new Object[]{
                   v.getIdOrdem(),
                   v.getNome_Cliente(),
                   v.getNome_Tecnico(), 
                   v.getDescricaoEquipamento(),
                   v.getRelato_cliente(),
                   v.getStatus(), 
                   v.getProblemaIdentificado(),
                   v.getRelatoServico(),});
            }
        } catch (Exception e) {
        }
    }
    
    
